                // 指定图表的配置项和数据
                var workChar = echarts.init(document.getElementById('workChar'), 'macarons');
                option = {
                    xAxis: {
                        type: 'category',
                        data: obj.data.title
                    },
                    yAxis: {
                        type: 'value'
                    },
                    legend: {
                        data: ['日计时总量']
                    },
                    series: [{
                        name: '日计时总量',
                        data: obj.data.sum,
                        label: {
                            normal: {
                                show: true,
                            }
                        },
                        type: 'line',
                        smooth: true
                    }]
                };
                workChar.setOption(option);

            } else {
                $.growl.error({message: "没有相关操作!"});
            }
        }
